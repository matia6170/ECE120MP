			ADD R6, R6, R1

			STR R3, R6, #0	;M[R6] <- R3

			ADD R1, R1, #-1

			BRzp CALCSTAOUT
;Loop
			AND R1, R1, #0 ;may be able to delete this line if above functions correctly

